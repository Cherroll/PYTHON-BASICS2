
fruits= ["apple","banana","cherry",]

fruits.append("kiwi") #adds kiwi to the list
print(fruits)

fruits.insert(1,"orange") #adds orange to the list at position (1)-meaning 2nd
print(fruits)

fruits.remove("kiwi") #removes the "kiwi that we added"
print(fruits)

fruits.sort(reverse=True) #descending order
print(fruits)

